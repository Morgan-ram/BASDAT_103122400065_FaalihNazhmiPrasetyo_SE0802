    ID_THEATER, 
    KELAS, 
    HARGA AS HARGA_AWAL, 
    (HARGA * 0.85) AS HARGA_DISKON
FROM
    THEATER_103122400065;
    

-- total pemasukan
    
SELECT
    T.ID_THEATER, 
    T.KELAS,
    NVL(SUM(T.HARGA), 0) AS TOTAL_PEMASUKAN
FROM
    THEATER_103122400065 T
LEFT JOIN 
    JADWAL_103122400065 J ON T.ID_THEATER = J.ID_THEATER
LEFT JOIN 
    TRANSAKSI_103122400065 TR ON J.ID_JADWAL = TR.ID_JADWAL
GROUP BY 
    T.ID_THEATER, T.KELAS
ORDER BY 
    T.ID_THEATER;